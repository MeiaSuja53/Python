# Tipos Primitivos e Saída de Dados: 
# int - usado para representar números inteiros: 1, -2, 3, etc.
# float - usado para representar números de ponto flutuante (decimais): -1.0, 2.5, 3.14, etc.
# str - usado para representar cadeias de caracteres (texto): "Olá", "Python", "123", etc.
# bool - usado para representar valores booleanos (True ou False): True, False.