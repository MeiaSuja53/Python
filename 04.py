# Exercicio 04 - peça ao usuário dois números inteiros e mostre a soma deles.
# int -  a função int() é usada para converter um valor para o tipo inteiro.
# input - A função input() é usada para ler a entrada do usuário.
# format - .format() é um método de formatação de strings que permite inserir valores em uma string formatada.
n1 = int(input ('Escolha um numero:'))
n2 = int(input ('Escolha outro numero:'))
r = n1 + n2
print('a soma dos numeros é:{}'.format(r))